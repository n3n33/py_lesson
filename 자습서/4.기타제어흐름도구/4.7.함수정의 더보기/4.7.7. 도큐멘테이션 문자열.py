# 4.7.7. 도큐멘테이션 문자열
# 여기에 도큐멘테이션 문자열의 내용과 포매팅에 관한 몇 가지 관례가 있습니다.
#
# 첫 줄은 항상 객체의 목적을 짧고, 간결하게 요약해야 합니다.
# 간결함을 위해, 객체의 이름이나 형을 명시적으로 언급하지 않아야 하는데,
# 이것들은 다른 방법으로 제공되기 때문입니다 (이름이 함수의 작업을 설명하는 동사라면 예외입니다).
# 이 줄은 대문자로 시작하고 마침표로 끝나야 합니다.
#
# 도큐멘테이션 문자열에 여러 줄이 있다면, 두 번째 줄은 비어있어서,
# 시각적으로 요약과 나머지 설명을 분리해야 합니다.
# 뒤따르는 줄들은 하나나 그 이상의 문단으로, 객체의 호출 규약, 부작용 등을 설명해야 합니다.